    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFrame marco = new JFrame("DialogDemo");
        marco.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        marco.pack();
        try{
            con=Principal.conexion(con);
            String insertar = "insert into Factura values(?,?,?,?)";
            PreparedStatement estado = con.prepareStatement(insertar);
            estado.setString(1, CodigoFactura.getText());
            estado.setString(2, CodigoEmpleado.getText());
            estado.setInt(3, Integer.parseInt(FechaExpedicion.getText()));
            estado.setString(4, CodigoPedido.getText());
            estado.execute();
            estado.close();
            JOptionPane.showMessageDialog(marco, "Registro insertado");
            con.close();
        }
        catch (Exception e){
            System.out.println("No se pudo guardar el registro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

