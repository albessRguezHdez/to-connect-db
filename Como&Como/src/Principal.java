    public static Connection conexion(Connection con) throws SQLException{
        try{
            //inicialización de la conexión a jdbc
            Class.forName("oracle.jdbc.OracleDriver");
            //driver manejador de la conexión a la bd, con los atributos de nombre de usuario y contraseña
            con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "sysdba", "123");
        }
        catch (ClassNotFoundException e){
            System.out.println("No se pudo realizar la conexion");
        }
        return con;
    }
    
    public static Statement State (Statement sta) throws SQLException{
        //inicializa la conexión
        con=conexion(con);
        //crea un estado para la conexión. en este caso es "conectado" de lo contrario 
        //no entra aca y lanza la excepción con el mensaje: "No se pudo realizar la conexion"
        sta=con.createStatement();
        //regresa el estado de conexión: "conectado" 
        return sta;
    }
    
//Resultset para cada tabla, código para realizar las consultas con "select * from ..."
    public static ResultSet Resu (ResultSet re) throws SQLException{
        //consulta para centro
        sta=State(sta);
        //Comando de consulta tipo Query + comando en oracle para mostrar
        re=sta.executeQuery("select * from Centro");
        return re;
    }

